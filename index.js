function getAllPagarConsultaMedico(cod_doctor, cod_grupo, valorPago){
    let nrVeces = 0;        
    for(let i=0; datosPrueba.length > i; i++){
        if (datosPrueba[i].cod_grupo === cod_grupo && datosPrueba[i].cod_medico === cod_doctor){
            nrVeces ++
        }
    }
    var pagarDoctor = nrVeces * valorPago;
    console.log(`El doctor ha atendido ${nrVeces} servicios y ganará ${pagarDoctor}$`);
}


function getAllPagarProcedimientosMedico(cod_doctor, cod_grupo, porcentajePago){
    let nrVeces = 0;
    let valoresPVP = [];        
    for(let i=0; datosPrueba.length > i; i++){
        if (datosPrueba[i].cod_grupo === cod_grupo && datosPrueba[i].cod_medico === cod_doctor){
            nrVeces ++;
            valoresPVP.push(datosPrueba[i].orden_pvp);
        }
    }
    // Calcular el total de valoresPVP
    
    let totalPVP = valoresPVP.reduce((acc, val) => acc + val, 0);
    // Calcular el pago al doctor basado en el porcentaje
    let pagarDoctor = totalPVP * (porcentajePago);
    console.log(`El doctor ha atendido ${nrVeces} procedimientos y ganará ${pagarDoctor.toFixed(2)}$`);
}

